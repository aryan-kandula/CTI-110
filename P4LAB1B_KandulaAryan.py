import turtle
win = turtle.Screen()  
t = turtle.Turtle()

t.pensize(10)
t.color("red")
t.penup()
t.goto(-100, 0)
t.pendown()
t.left(75)
t.forward(100)
t.right(150)
t.forward(100)
t.back(44)
t.right(105)
t.forward(30)

t.penup()
t.goto(50,100)
t.pendown()
t.left(90)
t.forward(100)
t.back(50)
t.left(140)
t.forward(65)
t.back(65)
t.right(100)
t.forward(65)

win.mainloop()
